# --- Functions ---
def detect_step():
    global steps, prev_y_accel, cooldown, pet_y_offset
    
    current_y = imu.get_accel_y()
    
    # Detect the difference between frames (The "Jerk")
    delta = abs(current_y - prev_y_accel)
    
    if delta > threshold and cooldown == 0:
        steps += 1
        cooldown = 8          # Prevent double-counting one step
        pet_y_offset = -10    # Make the alien "jump" visually
        piezo.tone(440, 50)   
    
    # Smoothly return the pet to ground after a jump
    if pet_y_offset < 0:
        pet_y_offset += 1
        
    if cooldown > 0:
        cooldown -= 1
        
    prev_y_accel = current_y
def draw_game():
    global reward_tracker
    display.fill(0)
    
    # Draw Alien in the center (jumping if step detected)
    display.blit(sprite_alien2, 54, 50 + pet_y_offset, 0)
    
    # Draw floor
    display.rect(0, 72, 128, 2, Display.Color.Green, True)
    
    # Draw Steps
    display.text("STEPS WALKED", 30, 90, Display.Color.White)
    display.text(str(steps), 60, 105, Display.Color.Cyan)
    
    # Celebrate every 25 steps
    if (steps % 25 == 0 and steps != 0 and reward_tracker):
        reward_tracker = False
        for i in range(6):
            leds[i].on()
            time.sleep_ms(100)
        for i in range(6):
            leds[i].off()
            time.sleep_ms(100)
    elif steps % 25 != 0:
        reward_tracker = True

def reset_steps():
    global steps
    steps = 0
